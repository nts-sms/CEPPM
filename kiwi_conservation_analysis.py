    print(f"  {'─'*58}")
    print(f"  {'Open foraging start':30} {X0:>12.3f} {X0:>14.3f}")
    print(f"  {'Open foraging final':30} {x_h[-1]:>12.3f} {x_hm[-1]:>14.3f}")
    print(f"  {'Change from start (pp)':30} {x_h_change*100:>+11.1f}% {x_hm_change*100:>+13.1f}%")
