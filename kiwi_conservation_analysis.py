    r, alpha, beta, delta = 0.05, 0.044, 0.0175, 0.35
    K_max, h              = 150, 0.1
    lv_model              = KiwiLotkaVolterraModel(r, alpha, beta, delta,
                                                   K_max=K_max, h=h,
                                                   r_stoat=R_STOAT, S_max=S_MAX)
    print(f"\n  Core parameters:")
    print(f"    r={r}  (kiwi intrinsic growth; DOC Recovery Plan 2018-2028)")
    print(f"    α={alpha} (predation rate; updated — reflects per-capita encounter")
    print(f"           rate consistent with <5% kiwi in stoat diet,")
    print(f"           Murphy & Dowding 1995; King & Powell 2007)")
    print(f"    β={beta} (stoat conversion efficiency; updated — kiwi contribute")
    print(f"           marginally to stoat reproduction; generalist diet)")
    print(f"    δ={delta}  (stoat natural death rate; annual survival=exp(-0.35)={np.exp(-0.35):.2f})")
    print(f"           Represents natural mortality only (disease, starvation, predation)")
    print(f"           NOT including trap mortality — that enters via harvest term h·S")
    print(f"           Source: King (1983); Powell & King (1997); annual survival ~70%")
    print(f"           With delta=0.35: NO coexistence without management — kiwi decline")
    print(f"           to extinction, consistent with observed 2% annual decline without"  )
    print(f"           predator control (McLennan et al. 1996; DOC Recovery Plan 2018)")
    print(f"\n  Extended model parameters:")
    print(f"    K_max={K_max}  (kiwi carrying capacity)")
    print(f"    h={h}      (Holling Type II handling time)")
    print(f"    r_stoat={R_STOAT}  (stoat baseline growth from non-kiwi prey)")
    print(f"    S_max={S_MAX}     (stoat non-kiwi carrying capacity)")

    K_eq, S_eq = lv_model.find_equilibrium()
    S_floor    = S_MAX * (1 - 0.35 / R_STOAT)
    print(f"\n  Coexistence equilibrium (no control): K={K_eq:.1f}, S={S_eq:.3f}")
    print(f"  Stoat floor (non-kiwi prey only):    K=0,  S={S_floor:.3f}")
    print(f"  Initial conditions: K0={K0}, S0={S0}, X0={X0}")

    # Hybrid model — uses SAME K0, S0 as L-V for consistency
    hybrid_model = KiwiHybridModel(egt_model, r, alpha, beta, delta,
                                   K_max=K_max, h=h,
                                   r_stoat=R_STOAT, S_max=S_MAX)

    # ========== STEP 2: VISUALISE ==========
    print("\n" + "="*70)
    print("STEP 2: BASE MODEL VISUALISATION")
    print("="*70)

    egt_model.plot_payoff_functions()
    egt_model.plot_phase_portrait()
    lv_model.plot_holling_response()
    print("\n  Demonstrating generalist stoat persistence behaviour...")
    lv_model.plot_stoat_persistence()   # NEW: shows stoats persist without kiwi
    lv_model.plot_phase_portrait_with_field(PHASE_PORTRAIT_ICS)

    # Baseline (no intervention) — uses config K0, S0
    t_base, K_base, S_base = lv_model.simulate_dynamics([K0, S0], T_MAX)
    lv_model.plot_time_series(t_base, K_base, S_base)

