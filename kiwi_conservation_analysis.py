class KiwiHybridModel:
    """
    Hybrid model combining EGT strategy dynamics with extended L-V population
    dynamics. Extended in v4 with DYNAMIC PAYOFF FEEDBACK.

    Previous version: payoff matrix was fixed — strategy evolution was
    decoupled from population state, causing open foraging to increase even
    as the population collapsed under predation pressure.

    This version: payoffs are adjusted dynamically based on current stoat
    density S. Higher stoat density penalises open foraging more than cover
    foraging, creating a biologically realistic feedback loop:

        High S → open foraging costly → strategy shifts toward cover
        Low S  → open foraging rewarding → strategy shifts toward open

    Predation penalty scales with stoat density and strategy vulnerability:
        open_penalty  = alpha_base * S * open_vulnerability_factor
        cover_penalty = alpha_base * S * cover_vulnerability_factor

    This ensures the EGT and L-V components respond to the same ecological
    conditions rather than running in parallel without meaningful coupling.
    """

    def __init__(self, egt_model, r_base, alpha_base, beta, delta,
                 K_max=150, h=0.1, r_stoat=0.6, S_max=3):
        self.egt_model    = egt_model
        # Hybrid base payoffs: near-neutral between strategies in the absence
        # of predation pressure. The sigmoid penalty system then tips the
        # balance — cover foraging is favoured when stoats are high (S>1),
        # open foraging is rewarded when stoats are controlled (S<1).
        # Calibrated so the strategy flip point (~S=1.5) is consistent with
        # the literature threshold of ~2 stoats/1,000 ha above which kiwi
        # chick recruitment fails (Arthur's Pass Wildlife Trust).
        self.base_payoffs = np.array([
            [0.35, 0.55],   # Open foraging: moderate reward vs both strategies
            [0.50, 0.22]    # Cover foraging: higher reward when open common,
                            # minimum viable (0.22) when cover common
        ])
        self.r_base       = r_base
        self.alpha_base   = alpha_base
        self.beta         = beta
        self.delta        = delta
        self.K_max        = K_max
        self.h            = h
        self.r_stoat      = r_stoat
        self.S_max        = S_max

    def predation_rate(self, alpha_eff, K):
        return (alpha_eff * K) / (1 + alpha_eff * self.h * K)

    def dynamic_payoffs(self, S):
        """
        Sigmoid predation penalty applied to base payoffs.

        Uses a logistic (sigmoid) function rather than a linear penalty,
        giving a smooth, bounded response to stoat density:

            penalty = max_penalty / (1 + exp(-k * (S - S_mid)))

        Parameters calibrated so that:
          S < 1.0: open foraging favoured (managed, low predation pressure)
          S ~ 1.5: strategies near-equal (transition zone)
          S > 2.0: cover foraging strongly favoured (high predation pressure)

        This flip point (~1.5 stoats/1,000 ha) is consistent with the
        literature threshold above which kiwi chick recruitment fails
        (Arthur's Pass Wildlife Trust; 2 stoats/1,000 ha maximum tolerable).

        Open foraging receives a larger maximum penalty (0.40) than cover
        foraging (0.15), reflecting the 30/70% vulnerability split.
        """
        k        = 3.0    # sigmoid steepness
        S_mid    = 1.0    # inflection point (stoats/1,000 ha)
        open_max = 0.40   # maximum penalty for open foraging
        cov_max  = 0.15   # maximum penalty for cover foraging

        sig      = 1 / (1 + np.exp(-k * (S - S_mid)))
        open_pen = open_max * sig
        cov_pen  = cov_max  * sig

        return np.array([
            [max(self.base_payoffs[0, 0] - open_pen, 0.01),
             max(self.base_payoffs[0, 1] - open_pen, 0.01)],
            [max(self.base_payoffs[1, 0] - cov_pen,  0.01),
             max(self.base_payoffs[1, 1] - cov_pen,  0.01)]
        ])

    def strategy_dependent_parameters(self, x):
        """
        Calculate effective L-V parameters based on current strategy mix.
        Uses base payoffs for the r_effective scaling (population-level effect).
        Dynamic payoffs handle the EGT strategy selection separately.
        """
        open_vulnerability  = 1.3
        cover_vulnerability = 0.7
        avg_vulnerability   = x * open_vulnerability + (1-x) * cover_vulnerability

        # Use base payoffs for average fitness scaling.
        # Normalisation constant PI_BAR_ESS = pi_bar(x*) is the average payoff
        # at the EGT equilibrium x*, computed dynamically from the base payoff
        # matrix so that it updates automatically if payoff values change.
        # At the ESS, pi_A(x*) = pi_B(x*) by definition, so pi_bar(x*) equals
        # the common equilibrium payoff. The ESS is derived from the payoff
        # matrix entries a,b,c,d as: x* = (d-b) / [(a-b) + (d-c)].
        # This ensures r_effective = r_base exactly when x = x*, and scales
        # smoothly below r_base as the population moves away from the ESS.
        a, b = self.base_payoffs[0]
        c, d = self.base_payoffs[1]
        denom = (a - b) + (d - c)
        x_star = (d - b) / denom if abs(denom) > 1e-10 else 0.5
        x_star = float(np.clip(x_star, 0.0, 1.0))
        PI_BAR_ESS       = self.egt_model.average_payoff(x_star)
        avg_reproduction = self.egt_model.average_payoff(x)

        return {
            'r_effective':     self.r_base * (avg_reproduction / PI_BAR_ESS),
            'alpha_effective': self.alpha_base * avg_vulnerability
        }

    def hybrid_system(self, t, y, harvest_rate=0):
        """
        Combined system with dynamic payoff feedback and optional harvest.

        dx/dt uses DYNAMIC payoffs (stoat-density-adjusted) so strategy
        selection responds to current predation pressure.

        dK/dt and dS/dt use strategy-dependent L-V parameters.

        harvest_rate: annual proportion of stoats removed by predator control.
        Uses same floor/harvest separation as KiwiLotkaVolterraModel —
        floor blocks only natural decline; harvest bypasses floor entirely.
        """
        x, K, S = y
        x = np.clip(x, 0.01, 0.99)
        K = max(K, 0)
        S = max(S, 0)

        # --- EGT replicator with dynamic payoffs ---
        dynamic_pm = self.dynamic_payoffs(S)
        self.egt_model.payoff_matrix = dynamic_pm

        pi_A   = self.egt_model.payoff_A(x)
        pi_avg = self.egt_model.average_payoff(x)
        dxdt   = x * (pi_A - pi_avg) if 0 < x < 1 else 0

        self.egt_model.payoff_matrix = self.base_payoffs

        # --- L-V population dynamics ---
        params = self.strategy_dependent_parameters(x)
        f_K    = self.predation_rate(params['alpha_effective'], K)
        dKdt   = params['r_effective'] * K * (1 - K / self.K_max) - f_K * S

        # Original formulation: r_stoat and delta kept separate
        # S_floor = S_max*(1-delta/r_stoat) = 12*(1-0.35/0.60) = 5.0
        natural_dSdt = (self.r_stoat * S * (1 - S / self.S_max)
                        + self.beta * f_K * S
                        - self.delta * S)
        S_floor = self.S_max * (1 - self.delta / self.r_stoat)
        non_kiwi_growth = self.r_stoat * S * (1 - S / self.S_max) - self.delta * S
        if S <= S_floor and non_kiwi_growth < 0:
            natural_dSdt = max(natural_dSdt, 0.0)
        harvest_dSdt = -harvest_rate * S
        dSdt = natural_dSdt + harvest_dSdt

        return [dxdt, dKdt, dSdt]

    def simulate_hybrid_dynamics(self, initial_conditions, t_max,
                                  intervention_time=None, harvest_rate=0):
        """
        Simulate the hybrid model with optional predator control intervention.

        Parameters:
        initial_conditions : [x0, K0, S0] — drawn from config block
        t_max              : simulation duration
        intervention_time  : year at which harvest begins (None = no intervention)
        harvest_rate       : annual stoat removal rate (0 = natural baseline)
        """
        x0, K0_h, S0_h = initial_conditions

        if intervention_time is None or intervention_time >= t_max:
            t_eval = np.linspace(0, t_max, 1000)
            sol    = solve_ivp(
                lambda t, y: self.hybrid_system(t, y, 0),
                [0, t_max], [x0, K0_h, S0_h],
                t_eval=t_eval, method='RK45'
            )
            self.egt_model.payoff_matrix = self.base_payoffs
            return sol.t, sol.y[0], sol.y[1], sol.y[2]

        # Two-phase simulation: before and after intervention
        t_before = np.linspace(0, intervention_time, 400)
        t_after  = np.linspace(intervention_time, t_max, 600)

        sol_b = solve_ivp(
            lambda t, y: self.hybrid_system(t, y, 0),
            [0, intervention_time], [x0, K0_h, S0_h],
            t_eval=t_before, method='RK45'
        )

        sol_a = solve_ivp(
            lambda t, y: self.hybrid_system(t, y, harvest_rate),
            [intervention_time, t_max],
            [sol_b.y[0][-1], sol_b.y[1][-1], sol_b.y[2][-1]],
            t_eval=t_after, method='RK45'
        )

        self.egt_model.payoff_matrix = self.base_payoffs
        return (np.concatenate([sol_b.t,    sol_a.t]),
                np.concatenate([sol_b.y[0], sol_a.y[0]]),
                np.concatenate([sol_b.y[1], sol_a.y[1]]),
                np.concatenate([sol_b.y[2], sol_a.y[2]]))

    def plot_hybrid_results(self, t, x, K, S,
                            t_m=None, x_m=None, K_m=None, S_m=None,
                            intervention_time=None, harvest_rate=None):
        """
        Plot hybrid model results.
        If managed trajectories (t_m, x_m, K_m, S_m) are provided,
        shows baseline vs managed comparison on each panel.
        """
        fig = plt.figure(figsize=(15, 12))
        S_floor = self.S_max * (1 - self.delta / self.r_stoat)
        has_managed = t_m is not None

        def add_managed(ax, t_m, vals_m, color='orange', label='With predator control'):
            if has_managed:
                ax.plot(t_m, vals_m, color=color, linewidth=2,
                       linestyle='--', label=label, alpha=0.85)

        # --- Strategy evolution ---
        ax = fig.add_subplot(2, 3, 1)
        ax.plot(t, x, 'b-', linewidth=2, label='No predator control')
        add_managed(ax, t_m, x_m)
        if intervention_time:
            ax.axvline(x=intervention_time, color='green', linestyle=':',
                      alpha=0.7, linewidth=1.5,
                      label=f'Control starts yr {intervention_time}')
        ax.set_xlabel('Time (years)', fontsize=11)
        ax.set_ylabel('Proportion Open Foraging', fontsize=11)
        ax.set_title('Strategy Evolution\n(Dynamic payoff feedback)', fontsize=12)
        ax.legend(fontsize=9)
        ax.grid(True, alpha=0.3)
