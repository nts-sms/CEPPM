    scenario_results = analyze_conservation_scenarios(hybrid_model)

    # ========== STEP 4: INTERVENTION (USER HARVEST RATE) ==========
    print("\n" + "="*70)
    print(f"STEP 4: INTERVENTION SIMULATION (harvest rate = {HARVEST_RATE:.2f})")
    print("="*70)

    t_ctrl, K_ctrl, S_ctrl = lv_model.simulate_dynamics(
        [K0, S0], T_MAX,
        intervention_time=INTERVENTION_TIME,
        harvest_rate=HARVEST_RATE
    )
    lv_model.plot_time_series(t_ctrl, K_ctrl, S_ctrl,
                             intervention_time=INTERVENTION_TIME,
                             harvest_rate=HARVEST_RATE)

    print(f"\nL-V results at year {T_MAX} (intervention at year {INTERVENTION_TIME}, h={HARVEST_RATE:.2f}):")
    print(f"  Kiwi:        {K_ctrl[-1]:.1f}  (no control: {K_base[-1]:.1f},  change: {((K_ctrl[-1]-K_base[-1])/K_base[-1]*100):+.1f}%)")
    print(f"  Stoats:      {S_ctrl[-1]:.3f}  (no control: {S_base[-1]:.3f})")
    S_floor_v = S_MAX * (1 - 0.35 / R_STOAT)
    h_crit_v  = R_STOAT - delta
    if HARVEST_RATE >= R_STOAT:
        print(f"  Stoat regime: ERADICATION (h={HARVEST_RATE:.2f} ≥ r_stoat={R_STOAT})")
    elif HARVEST_RATE > h_crit_v:
        print(f"  Stoat regime: floor-suppressed (h={HARVEST_RATE:.2f} > h_crit={h_crit_v:.2f})")
    else:
        print(f"  Stoat regime: coexistence (h={HARVEST_RATE:.2f} ≤ h_crit={h_crit_v:.2f})")

    # EGT behavioural response
    int_payoffs = kiwi_payoffs + np.array([[0.117, 0.275], [0.062, 0.02]])
    t_egt, x_egt = egt_model.simulate_dynamics(
        X0, T_MAX, intervention_time=INTERVENTION_TIME, new_payoffs=int_payoffs
    )

    # Find x just before intervention for comparison
    x_pre_intervention = x_egt[np.searchsorted(t_egt, INTERVENTION_TIME) - 1]
    x_final = x_egt[-1]
    x_change = x_final - X0
    x_change_post = x_final - x_pre_intervention

    print(f"\nEGT strategy results:")
    print(f"  Open foraging start:               {X0:.3f}  ({X0*100:.1f}%)")
    print(f"  Open foraging at intervention yr:  {x_pre_intervention:.3f}  ({x_pre_intervention*100:.1f}%)")
    print(f"  Open foraging final (yr {T_MAX}):      {x_final:.3f}  ({x_final*100:.1f}%)")
    print(f"  Change from start:                 {x_change:+.3f}  ({x_change*100:+.1f}pp)")
    print(f"  Change due to intervention:        {x_change_post:+.3f}  ({x_change_post*100:+.1f}pp)")

    fig, ax = plt.subplots(figsize=(10, 6))
    ax.plot(t_egt, x_egt, 'b-', linewidth=2)
    ax.axvline(x=INTERVENTION_TIME, color='green', linestyle='--',
               linewidth=2, label=f'Predator Control starts (yr {INTERVENTION_TIME}, h={HARVEST_RATE:.2f})')
    ax.axhline(y=X0, color='gray', linestyle=':', alpha=0.7,
               label=f'Starting proportion ({X0:.2f})')
    ax.axhline(y=x_final, color='blue', linestyle=':', alpha=0.7,
               label=f'Final proportion ({x_final:.3f}, {x_change*100:+.1f}pp from start)')
    ax.annotate(f'{x_change*100:+.1f}pp\nfrom start',
                xy=(T_MAX*0.85, (X0+x_final)/2),
                fontsize=10, color='blue',
                arrowprops=dict(arrowstyle='->', color='blue'),
                xytext=(T_MAX*0.85, (X0+x_final)/2))
    ax.set_xlabel('Time (years)', fontsize=12)
    ax.set_ylabel('Proportion Open Foraging', fontsize=12)
    ax.set_title('EGT: Behavioural Response to Predator Control\n'
                f'Open foraging {x_change*100:+.1f}pp from start ({X0:.2f} → {x_final:.3f})',
                fontsize=13)
    ax.legend(fontsize=10)
