def run_complete_kiwi_analysis():

    print("\n" + "="*70)
    print("STEP 1: MODEL INITIALIZATION")
    print("="*70)

    # EGT model
    # EGT model — updated to new baseline payoffs [[0.35,0.55],[0.50,0.22]]
