class KiwiLotkaVolterraModel:
    """
    Extended Lotka-Volterra model with:
    1. Logistic prey growth:       dK/dt = rK(1 - K/K_max) - f(K)·S
    2. Holling Type II predation:  f(K) = αK / (1 + αhK)
    3. Generalist stoat equation:  dS/dt = r_stoat·S·(1 - S/S_max)
                                          + β·f(K)·S - δ·S - h·S

    Extension 3 (new in v3):
    ─────────────────────────
    Previously stoat growth depended solely on kiwi consumption (β·f(K)·S),
    meaning stoats declined whenever kiwi declined. This is ecologically
    incorrect: stoats are generalist predators for whom kiwi represent less
    than 5% of diet (Murphy & Dowding 1995). The new term r_stoat·S·(1-S/S_max)
    gives stoats an independent logistic growth from their broader prey base
    (rodents, invertebrates). Kiwi predation adds on top of this baseline.

    Biological interpretation:
      r_stoat : stoat intrinsic growth rate from non-kiwi prey
      S_max   : stoat carrying capacity supported by non-kiwi prey alone
    When kiwi are scarce, stoats persist at S_max (set by rodent availability).
    When kiwi are abundant, stoat population can exceed S_max temporarily.
    """

    def __init__(self, r, alpha, beta, delta, K_max=150, h=0.1,
                 r_stoat=0.3, S_max=10):
        self.r       = r
        self.alpha   = alpha
        self.beta    = beta
        self.delta   = delta
        self.K_max   = K_max
        self.h       = h
        self.r_stoat = r_stoat  # NEW: stoat baseline growth from non-kiwi prey
        self.S_max   = S_max    # NEW: stoat carrying capacity from non-kiwi prey

    def predation_rate(self, K):
        return (self.alpha * K) / (1 + self.alpha * self.h * K)

    def system_equations(self, t, y, harvest_rate=0):
        """
        Kiwi:  dK/dt = rK(1 - K/K_max) - f(K)·S
        Stoat: dS/dt = r_stoat·S·(1-S/S_max) + β·f(K)·S - δ·S - h·S

        ORIGINAL FORMULATION (restored from Option A):
        ───────────────────────────────────────────────
        r_stoat and delta are kept SEPARATE in the logistic term so their
        biological distinction is preserved in the code. The observable
        stoat equilibrium without management is:

            S_floor = S_max × (1 - δ/r_stoat) = 12 × (1 - 0.35/0.60) = 5.0

        S_max = 12 is a latent resource ceiling (not directly observable).
        S_floor = 5.0 is the directly observable non-mast equilibrium density,
        matching Murphy & Dowding (1994, 1995): 5-8 stoats/1,000 ha.

        S₀ = 3 < S_floor = 5 → stoats correctly increase toward 5 in the
        unmanaged baseline.

        The floor logic is restored: floor enforcement prevents stoats
        declining below S_floor due to kiwi scarcity alone (non-kiwi prey
        always sustains them at S_floor). Harvest bypasses the floor entirely.

        Three regimes:
          h = 0             : stoats settle at S_floor from non-kiwi prey
          0 < h < h_crit    : stoats suppressed; kiwi may recover
          h >= h_crit=0.25  : net stoat decline — eradication possible
        """
        K, S = y
        K = max(K, 0)
        S = max(S, 0)
        f_K = self.predation_rate(K)

        dKdt = self.r * K * (1 - K / self.K_max) - f_K * S

        natural_dSdt = (self.r_stoat * S * (1 - S / self.S_max)
                        + self.beta * f_K * S
                        - self.delta * S)

        # Floor enforcement — prevents stoats declining below S_floor
        # due to kiwi scarcity. Non-kiwi prey sustains stoats at S_floor.
        # Harvest bypasses floor entirely.
        S_floor = self.S_max * (1 - self.delta / self.r_stoat)
        non_kiwi_growth = self.r_stoat * S * (1 - S / self.S_max) - self.delta * S
        if S <= S_floor and non_kiwi_growth < 0:
            natural_dSdt = max(natural_dSdt, 0.0)

        harvest_dSdt = -harvest_rate * S
        dSdt = natural_dSdt + harvest_dSdt

        return [dKdt, dSdt]

    def find_equilibrium(self, harvest_rate=0, suppress_warnings=False):
        """
        Numerical equilibrium finder (original formulation restored).

        Four possible outcomes based on nullcline analysis:
        ────────────────────────────────────────────────
        1. COEXISTENCE equilibrium (K>0, S>0): found numerically where both
           nullclines intersect. Exists in partial recovery zone (h < h_crit)
           and kiwi bonus zone (h_crit < h < h_erad).

        2. KIWI BONUS ZONE (h_crit < h < h_erad ≈ 0.32):
           Non-kiwi stoat equilibrium S*(h) < 0 but stoats persist at a
           reduced positive equilibrium through the kiwi predation bonus
           β·f(K)·S as kiwi recover under predator control. The numerical
           solver above should find this intersection; this fallback applies
           if it does not converge.
           h_crit  = r_stoat - delta = 0.25
           h_erad  = r_stoat - delta + β·f(K_max) ≈ 0.32

        3. FULL SUPPRESSION (h_erad ≤ h < r_stoat): stoat nullcline
           non-positive across full kiwi density range; stoats driven to
           zero; kiwi recover to K_max.

        4. THEORETICAL UPPER BOUND (h ≥ r_stoat = 0.60): harvest exceeds
           total stoat intrinsic growth capacity; PF2050 scenario.
        """
        S_floor  = self.S_max * (1 - self.delta / self.r_stoat)
        h_crit   = self.r_stoat - self.delta
        f_Kmax   = (self.alpha * self.K_max
                    / (1 + self.alpha * self.h * self.K_max))
        h_erad   = h_crit + self.beta * f_Kmax   # ≈ 0.320

        def equations(vars):
            K, S = vars
            K = max(K, 1e-6)
            S = max(S, 1e-6)
            f_K  = self.predation_rate(K)
            eq1  = self.r * K * (1 - K / self.K_max) - f_K * S
            eq2  = (self.r_stoat * S * (1 - S / self.S_max)
                    + self.beta * f_K * S
                    - self.delta * S
                    - harvest_rate * S)
            return [eq1, eq2]

        # Extend initial guesses to cover kiwi bonus zone (S near zero, K large)
        best = None
        best_residual = np.inf
        K_guesses = [10, 20, 30, 50, 80, 100, 120, 140]
        S_guesses = [0.05, 0.1, 0.3, 0.5, 1.0, 2.0, 3.0, 4.0]
        for K_g in K_guesses:
            for S_g in S_guesses:
                try:
                    sol = fsolve(equations, [K_g, S_g], full_output=True)
                    K_eq, S_eq = sol[0]
                    residual = max(abs(x) for x in equations(sol[0]))
                    if (K_eq > 1 and S_eq > 1e-4
                            and K_eq < self.K_max
                            and residual < 1e-6):
                        if residual < best_residual:
                            best = (K_eq, S_eq)
                            best_residual = residual
                except:
                    pass

        if best is not None:
            return best[0], best[1]

        # No coexistence found — determine regime from nullcline analysis
        if harvest_rate >= h_erad:
            # Full suppression or theoretical upper bound —
            # stoat nullcline non-positive everywhere; stoats eradicated
            return self.K_max, 0.0
        elif harvest_rate > h_crit:
            # Kiwi bonus zone — numerical solver failed to find intersection;
            # return approximate kiwi bonus equilibrium as fallback
            # (S small positive, K large but < K_max)
            if not suppress_warnings:
                print(f"  NOTE: kiwi bonus zone (h={harvest_rate:.2f}, "
                      f"h_crit={h_crit:.2f}, h_erad={h_erad:.3f}) — "
                      f"numerical solver did not converge; returning approximation.")
            return self.K_max * 0.95, 0.05
        else:
            if not suppress_warnings:
                print(f"  NOTE: no coexistence equilibrium (h={harvest_rate:.2f}, "
                      f"h_crit={h_crit:.2f}). Kiwi decline toward extinction.")
            return 0.0, S_floor

    def simulate_dynamics(self, initial_conditions, t_max,
                         intervention_time=None, harvest_rate=0):
        """Uses K0, S0 from initial_conditions argument (set from config block)."""
        K0_sim, S0_sim = initial_conditions

        if intervention_time is None or intervention_time >= t_max:
            t_eval = np.linspace(0, t_max, 1000)
            sol = solve_ivp(lambda t, y: self.system_equations(t, y, 0),
                           [0, t_max], [K0_sim, S0_sim],
                           t_eval=t_eval, method='RK45')
            return sol.t, sol.y[0], sol.y[1]

        t_before = np.linspace(0, intervention_time, 500)
        t_after  = np.linspace(intervention_time, t_max, 500)

        sol_b = solve_ivp(lambda t, y: self.system_equations(t, y, 0),
                         [0, intervention_time], [K0_sim, S0_sim],
                         t_eval=t_before, method='RK45')

        sol_a = solve_ivp(lambda t, y: self.system_equations(t, y, harvest_rate),
                         [intervention_time, t_max],
                         [sol_b.y[0][-1], sol_b.y[1][-1]],
                         t_eval=t_after, method='RK45')

        return (np.concatenate([sol_b.t,    sol_a.t]),
                np.concatenate([sol_b.y[0], sol_a.y[0]]),
                np.concatenate([sol_b.y[1], sol_a.y[1]]))

