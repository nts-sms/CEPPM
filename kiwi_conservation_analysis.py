def payoff_sensitivity_analysis_full():
    print("\n" + "="*70)
    print("PAYOFF MATRIX SENSITIVITY ANALYSIS")
    print("="*70)

    # All scenarios proportionally scaled from old values using
    # element-wise scale factors [[1.167, 0.917], [1.25, 1.0]]
    # derived from new_baseline / old_baseline.
    # d=0.22 (minimum viable survival) held fixed across all scenarios.
    # risk_seeking produces a corner solution (x=1.0) in both old and new
    # versions — this is expected behaviour for a strongly open-dominant matrix.
    scenarios = {
        'conservative': np.array([[0.292, 0.321], [0.375, 0.22]]),
        'moderate':     np.array([[0.35,  0.55 ], [0.50,  0.22]]),  # = new baseline
        'extreme':      np.array([[0.233, 0.733], [0.625, 0.22]]),
        'risk_averse':  np.array([[0.292, 0.413], [0.50,  0.22]]),
        'risk_seeking': np.array([[0.408, 0.596], [0.375, 0.22]])
    }

    results = {}
    for name, payoffs in scenarios.items():
        model = KiwiEGTModel(payoffs)
        eq    = model.find_equilibrium()
        if eq is None or not 0 <= eq <= 1:
            results[name] = None; continue
        stability   = model.stability_analysis(eq)
        avg_fitness = model.average_payoff(eq)
        # Intervention effect proportionally scaled from old [[0.10,0.30],[0.05,0.02]]
        new_payoffs = payoffs + np.array([[0.117, 0.275], [0.062, 0.02]])
        m2          = KiwiEGTModel(new_payoffs)
        eq2         = m2.find_equilibrium()
        sc          = eq2 - eq if eq2 and 0 <= eq2 <= 1 else None
        fc          = m2.average_payoff(eq2) - avg_fitness if sc is not None else None
        results[name] = {
            'payoffs': payoffs, 'equilibrium': eq, 'stability': stability,
            'avg_fitness': avg_fitness, 'strategy_change': sc, 'fitness_change': fc
        }
        print(f"\n{name.upper()}: eq={eq:.3f} [{stability}], fitness={avg_fitness:.3f}")
        if sc is not None:
            print(f"  After intervention: shift={sc:+.3f}, Δfitness={fc:+.3f}")

    valid = {k: v for k, v in results.items() if v}
    if valid:
        eqs = [v['equilibrium'] for v in valid.values()]
        cv  = np.std(eqs) / np.mean(eqs) * 100
        print(f"\nCV across scenarios: {cv:.1f}% — "
              f"{'ROBUST' if cv<20 else 'MODERATE' if cv<40 else 'SENSITIVE'}")
    return results


