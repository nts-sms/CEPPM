def sensitivity_analysis(lv_model):
    print("\n" + "="*70)
    print("SENSITIVITY ANALYSIS")
    print("="*70)
    print("  Note: boundary regions (no coexistence) are shown as gaps/shading.")
    print("  These are biologically meaningful limits, not model errors.\n")

    base = {'r': lv_model.r, 'alpha': lv_model.alpha,
            'beta': lv_model.beta, 'delta': lv_model.delta}
    ranges = {
        'r':     np.linspace(0.1,  0.5,  30),
        'alpha': np.linspace(0.01, 0.1,  30),
        'beta':  np.linspace(0.005,0.05, 30),
        'delta': np.linspace(0.2,  0.6,  30)
    }

    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    axes = axes.flatten()
    results = {}

    for idx, (param, vals) in enumerate(ranges.items()):
        k_eqs        = []
        n_boundary   = 0

        for v in vals:
            p = base.copy(); p[param] = v
            m = KiwiLotkaVolterraModel(**p, K_max=lv_model.K_max, h=lv_model.h,
                                       r_stoat=lv_model.r_stoat, S_max=lv_model.S_max)
            # suppress_warnings=True: boundary conditions are expected in a sweep
            K_eq, _ = m.find_equilibrium(0, suppress_warnings=True)

            if K_eq >= lv_model.K_max * 0.99:
                # Boundary: no coexistence — kiwi decline to extinction
                k_eqs.append(np.nan)
                n_boundary += 1
            else:
                k_eqs.append(K_eq)
        results[param] = {'values': vals, 'kiwi_equilibria': k_eqs,
                         'n_boundary': n_boundary}

        # Sensitivity coefficient at baseline (skip if baseline is at boundary)
        bi = np.argmin(np.abs(vals - base[param]))
        if 0 < bi < len(k_eqs) - 1:
            k_lo, k_hi, k_c = k_eqs[bi-1], k_eqs[bi+1], k_eqs[bi]
            if not any(np.isnan(v) for v in [k_lo, k_hi, k_c]) and k_c > 0:
                s = ((k_hi - k_lo) / k_c) / ((vals[bi+1] - vals[bi-1]) / base[param])
                print(f"  {param}: 1% change → {s:.2f}% change in K*")
            else:
                print(f"  {param}: baseline is at boundary — sensitivity not defined")

        if n_boundary > 0:
            pct = n_boundary / len(vals) * 100
            print(f"  {param}: {n_boundary}/{len(vals)} ({pct:.0f}%) values outside "
                  f"coexistence regime (delta >= r_stoat boundary)")

        # Plot with boundary shading
        ax = axes[idx]
        ax.plot(vals, k_eqs, 'b-', linewidth=2)
        ax.axvline(x=base[param], color='red', linestyle='--',
                  alpha=0.7, linewidth=2, label='Baseline')
        boundary_vals = [v for v, k in zip(vals, k_eqs) if np.isnan(k)]
        if boundary_vals:
            ax.axvspan(min(boundary_vals), max(boundary_vals),
                      alpha=0.12, color='red',
                      label=f'No coexistence ({n_boundary} values)')
        ax.set_xlabel(param, fontsize=11)
        ax.set_ylabel('Kiwi Equilibrium K*', fontsize=11)
        ax.set_title(f'Sensitivity to {param}', fontsize=12)
        ax.legend(fontsize=9)
        ax.grid(True, alpha=0.3)

    plt.suptitle('L-V Parameter Sensitivity Analysis\n'
                'Red shading = no coexistence equilibrium (parameter boundary)',
                fontsize=12)
    plt.tight_layout()
    plt.show()
    return results


