def analyze_field_data():
    print("\n" + "="*70)
    print("FIELD DATA ANALYSIS (simulated)")
    print("="*70)
    np.random.seed(42)
    n = 120
    strategies = np.random.choice([0, 1], n, p=[0.65, 0.35])
