    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    axes[0,0].hist(data[data['strategy']==0]['fitness'], bins=15, alpha=0.7, label='Cover')
    axes[0,0].hist(data[data['strategy']==1]['fitness'], bins=15, alpha=0.7, label='Open')
    axes[0,0].set_title('Fitness Distribution'); axes[0,0].legend()
    data.boxplot('fitness', by='strategy', ax=axes[0,1])
