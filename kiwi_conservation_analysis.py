    if HARVEST_RATE >= R_STOAT:
        print(f"  Stoat regime: THEORETICAL UPPER BOUND "
              f"(h={HARVEST_RATE:.2f} ≥ h_erad_theo={R_STOAT:.2f})")
    elif HARVEST_RATE >= h_erad_v:
        print(f"  Stoat regime: FULL SUPPRESSION — stoats eradicated "
              f"(h={HARVEST_RATE:.2f} ≥ h_erad={h_erad_v:.3f})")
    elif HARVEST_RATE > h_crit_v:
        print(f"  Stoat regime: KIWI BONUS ZONE — stoats persist through kiwi bonus "
              f"(h_crit={h_crit_v:.2f} < h={HARVEST_RATE:.2f} < h_erad={h_erad_v:.3f})")
    else:
        print(f"  Stoat regime: COEXISTENCE / PARTIAL RECOVERY "
              f"(h={HARVEST_RATE:.2f} ≤ h_crit={h_crit_v:.2f})")

    # EGT behavioural response
    int_payoffs = kiwi_payoffs + np.array([[0.117, 0.275], [0.062, 0.02]])
    t_egt, x_egt = egt_model.simulate_dynamics(
        X0, T_MAX, intervention_time=INTERVENTION_TIME, new_payoffs=int_payoffs
    )

    # Find x just before intervention for comparison
    x_pre_intervention = x_egt[np.searchsorted(t_egt, INTERVENTION_TIME) - 1]
    x_final = x_egt[-1]
    x_change = x_final - X0
    x_change_post = x_final - x_pre_intervention

    print(f"\nEGT strategy results:")
    print(f"  Open foraging start:               {X0:.3f}  ({X0*100:.1f}%)")
    print(f"  Open foraging at intervention yr:  {x_pre_intervention:.3f}  ({x_pre_intervention*100:.1f}%)")
    print(f"  Open foraging final (yr {T_MAX}):      {x_final:.3f}  ({x_final*100:.1f}%)")
    print(f"  Change from start:                 {x_change:+.3f}  ({x_change*100:+.1f}pp)")
    print(f"  Change due to intervention:        {x_change_post:+.3f}  ({x_change_post*100:+.1f}pp)")

    fig, ax = plt.subplots(figsize=(10, 6))
    ax.plot(t_egt, x_egt, 'b-', linewidth=2)
    ax.axvline(x=INTERVENTION_TIME, color='green', linestyle='--',
               linewidth=2, label=f'Predator Control starts (yr {INTERVENTION_TIME}, h={HARVEST_RATE:.2f})')
    ax.axhline(y=X0, color='gray', linestyle=':', alpha=0.7,
               label=f'Starting proportion ({X0:.2f})')
    ax.axhline(y=x_final, color='blue', linestyle=':', alpha=0.7,
               label=f'Final proportion ({x_final:.3f}, {x_change*100:+.1f}pp from start)')
    ax.annotate(f'{x_change*100:+.1f}pp\nfrom start',
                xy=(T_MAX*0.85, (X0+x_final)/2),
                fontsize=10, color='blue',
                arrowprops=dict(arrowstyle='->', color='blue'),
                xytext=(T_MAX*0.85, (X0+x_final)/2))
    ax.set_xlabel('Time (years)', fontsize=12)
    ax.set_ylabel('Proportion Open Foraging', fontsize=12)
    ax.set_title('EGT: Behavioural Response to Predator Control\n'
                f'Open foraging {x_change*100:+.1f}pp from start ({X0:.2f} → {x_final:.3f})',
                fontsize=13)
    ax.legend(fontsize=10)
    ax.grid(True, alpha=0.3)
    ax.set_ylim(0, 1)
    plt.tight_layout()
    plt.show()

    # ========== STEP 5: HYBRID MODEL ==========
    print("\n" + "="*70)
    print("STEP 5: HYBRID MODEL")
    print("="*70)

    # Baseline: no predator control
    print("\nRunning baseline hybrid (no predator control)...")
    t_h, x_h, K_h, S_h = hybrid_model.simulate_hybrid_dynamics(
        [X0, K0, S0], T_MAX
    )

    # Managed: with user harvest rate and intervention time
    print(f"Running managed hybrid (h={HARVEST_RATE:.2f}, intervention yr {INTERVENTION_TIME})...")
    t_hm, x_hm, K_hm, S_hm = hybrid_model.simulate_hybrid_dynamics(
        [X0, K0, S0], T_MAX,
        intervention_time=INTERVENTION_TIME,
        harvest_rate=HARVEST_RATE
    )

    hybrid_model.plot_hybrid_results(
        t_h, x_h, K_h, S_h,
        t_m=t_hm, x_m=x_hm, K_m=K_hm, S_m=S_hm,
        intervention_time=INTERVENTION_TIME,
        harvest_rate=HARVEST_RATE
    )

    x_h_change  = x_h[-1]  - X0
    x_hm_change = x_hm[-1] - X0
    x_hm_post   = x_hm[-1] - x_hm[np.searchsorted(t_hm, INTERVENTION_TIME) - 1]

    print(f"\nHybrid model results at year {T_MAX}:")
    print(f"  {'':30} {'No control':>12} {'With control':>14}")
    print(f"  {'─'*58}")
    print(f"  {'Open foraging start':30} {X0:>12.3f} {X0:>14.3f}")
    print(f"  {'Open foraging final':30} {x_h[-1]:>12.3f} {x_hm[-1]:>14.3f}")
    print(f"  {'Change from start (pp)':30} {x_h_change*100:>+11.1f}% {x_hm_change*100:>+13.1f}%")
    print(f"  {'Post-intervention shift (pp)':30} {'—':>12} {x_hm_post*100:>+13.1f}%")
    print(f"  {'Kiwi population':30} {K_h[-1]:>12.1f} {K_hm[-1]:>14.1f}")
    print(f"  {'Stoat population':30} {S_h[-1]:>12.3f} {S_hm[-1]:>14.3f}")

    # ========== STEP 6-7: SENSITIVITY + FIELD DATA ==========
    sens_results = sensitivity_analysis(lv_model)
    field_data, pop_data = analyze_field_data()

    # ========== STEP 5B: FORAGING vs HARVEST RATE SWEEP ==========
    print("\n" + "="*70)
    print("STEP 5B: FORAGING STRATEGY vs HARVEST RATE — FOUR-ZONE SWEEP")
    print("="*70)
    foraging_sweep_h, foraging_sweep_x = plot_foraging_vs_harvest_rate(
        hybrid_model, intervention_year=5, t_max=T_MAX
    )

    # ========== STEP 5C: THREE-MODEL COMPARISON ==========
    print("\n" + "="*70)
    print("STEP 5C: THREE-MODEL COMPARISON (EGT | L-V | CEPPM)")
    print("="*70)
    three_model_results = three_model_comparison(hybrid_model, t_max=T_MAX)

    # ========== STEP 8: SUMMARY ==========
    print("\n" + "="*70)
    print("SUMMARY")
    print("="*70)
    print(f"\nInitial conditions (all models consistent):")
    print(f"  K0={K0}, S0={S0}, X0={X0} ({X0*100:.0f}% open foraging)")
    print(f"  Intervention year: {INTERVENTION_TIME},  Harvest rate: {HARVEST_RATE:.2f}")
    print(f"\nL-V model outcomes at year {T_MAX}:")
    print(f"  No control:   K={K_base[-1]:.1f}, S={S_base[-1]:.3f}")
    print(f"  With control: K={K_ctrl[-1]:.1f}, S={S_ctrl[-1]:.3f}  ({((K_ctrl[-1]-K_base[-1])/K_base[-1]*100):+.1f}% kiwi change)")
    print(f"\nEGT strategy outcomes at year {T_MAX}:")
    print(f"  Open foraging: {X0:.3f} → {x_final:.3f}  ({(x_final-X0)*100:+.1f}pp change from start)")
    print(f"  Intervention effect: {x_pre_intervention:.3f} → {x_final:.3f}  ({(x_final-x_pre_intervention)*100:+.1f}pp post-intervention shift)")
    print(f"\nHybrid model (no control) at year {T_MAX}:")
    print(f"  Open foraging: {X0:.3f} → {x_h[-1]:.3f}  ({x_h_change*100:+.1f}pp)")
    print(f"Hybrid model (h={HARVEST_RATE:.2f}, intervention yr {INTERVENTION_TIME}) at year {T_MAX}:")
    print(f"  Open foraging: {X0:.3f} → {x_hm[-1]:.3f}  ({x_hm_change*100:+.1f}pp)")
    print(f"  Post-intervention strategy shift: {x_hm_post*100:+.1f}pp")
    print(f"\nGeneralist stoat thresholds (updated — nullcline analysis):")
    S_floor_val  = S_MAX*(1-0.35/R_STOAT)
    h_crit_v     = R_STOAT-delta
    f_Kmax_v     = 0.044*150/(1+0.044*0.1*150)
    h_erad_v     = h_crit_v+0.0175*f_Kmax_v
    print(f"  S_floor={S_floor_val:.3f}  h_crit={h_crit_v:.3f}  "
          f"h_erad={h_erad_v:.4f}  h_erad_theo={R_STOAT:.2f}")
    if HARVEST_RATE >= R_STOAT:
        print(f"  → THEORETICAL ERADICATION REGIME at h={HARVEST_RATE:.2f} "
              f"(h ≥ h_erad_theo={R_STOAT})")
    elif HARVEST_RATE >= h_erad_v:
        print(f"  → FULL SUPPRESSION at h={HARVEST_RATE:.2f} "
              f"(h ≥ h_erad={h_erad_v:.3f})")
    elif HARVEST_RATE > h_crit_v:
        print(f"  → KIWI BONUS ZONE at h={HARVEST_RATE:.2f} "
              f"(h_crit={h_crit_v:.2f} < h < h_erad={h_erad_v:.3f})")
        print(f"     Stoats persist through kiwi predation bonus despite "
              f"non-kiwi equilibrium collapse")
    else:
        print(f"  → COEXISTENCE / PARTIAL RECOVERY at h={HARVEST_RATE:.2f} "
              f"(h ≤ h_crit={h_crit_v:.2f})")
    print(f"\nRemaining limitations:")
    print(f"  • Beech mast not explicitly modelled (r_stoat increases during mast)")
    print(f"  • Spatial reinvasion not modelled — real eradication harder than h > r_stoat")
    print(f"  • r_stoat and S_max need empirical calibration to rodent data")
    print(f"  • EGT intervention payoffs need empirical validation (SS flag)")
    print("\n" + "="*70)
    print("ANALYSIS COMPLETE")
    print("="*70)

    return {
        'egt_model':            egt_model,
        'lv_model':             lv_model,
        'hybrid_model':         hybrid_model,
        'scenario_results':     scenario_results,
        'sensitivity_results':  sens_results,
        'field_data':           field_data,
        'population_data':      pop_data,
        'foraging_sweep_h':     foraging_sweep_h,
        'foraging_sweep_x':     foraging_sweep_x,
        'three_model_results':  three_model_results,
    }


