class KiwiEGTModel:
    """
    EGT model for kiwi foraging strategies.
    Strategy A: Open foraging (higher food intake, higher predation risk)
    Strategy B: Cover foraging (lower food intake, lower predation risk)
    """

    def __init__(self, payoff_matrix):
        self.payoff_matrix = np.array(payoff_matrix)

    def payoff_A(self, x):
        a, b = self.payoff_matrix[0]
        return x * a + (1 - x) * b

    def payoff_B(self, x):
        c, d = self.payoff_matrix[1]
        return x * c + (1 - x) * d

    def average_payoff(self, x):
        return x * self.payoff_A(x) + (1 - x) * self.payoff_B(x)

    def replicator_dynamics(self, x, t):
        if x <= 0 or x >= 1:
            return 0
        return x * (self.payoff_A(x) - self.average_payoff(x))

    def find_equilibrium(self):
        a, b, c, d = self.payoff_matrix.flatten()
        denominator = (a - b) + (d - c)
        if abs(denominator) < 1e-10:
            return None
        x_star = (d - b) / denominator
        if 0 <= x_star <= 1:
            return x_star
        return 1.0 if self.payoff_A(1) > self.payoff_B(1) else 0.0

    def stability_analysis(self, x_star):
        h = 1e-8
        derivative = (self.replicator_dynamics(x_star + h, 0) -
                     self.replicator_dynamics(x_star - h, 0)) / (2 * h)
        return f"Stable; derivative = {derivative:.6f}" if derivative < 0 \
               else f"Unstable; derivative = {derivative:.6f}"

    def simulate_dynamics(self, x0, t_max, intervention_time=None, new_payoffs=None):
        t_span = np.linspace(0, t_max, 500)
        if intervention_time is None or intervention_time >= t_max:
            solution = odeint(self.replicator_dynamics, x0, t_span)
            return t_span, solution.flatten()

        t_before = t_span[t_span < intervention_time]
        t_after  = t_span[t_span >= intervention_time]
        sol_before = odeint(self.replicator_dynamics, x0, t_before)

        old_payoffs = self.payoff_matrix.copy()
        self.payoff_matrix = new_payoffs
        x_at = sol_before[-1] if len(sol_before) > 0 else x0
        sol_after = odeint(self.replicator_dynamics, x_at, t_after - intervention_time)
        self.payoff_matrix = old_payoffs

        return np.concatenate([t_before, t_after]), \
               np.concatenate([sol_before.flatten(), sol_after.flatten()])

    def plot_phase_portrait(self):
        x_range = np.linspace(0.01, 0.99, 100)
        dx_dt = [self.replicator_dynamics(x, 0) for x in x_range]
        plt.figure(figsize=(10, 6))
        plt.plot(x_range, dx_dt, 'b-', linewidth=2)
        plt.axhline(y=0, color='k', linestyle='--', alpha=0.5)
        eq = self.find_equilibrium()
        if eq is not None and 0 < eq < 1:
            plt.axvline(x=eq, color='r', linestyle='--', alpha=0.7, linewidth=2,
                       label=f'Equilibrium: {eq:.3f}')
            plt.text(eq, max(dx_dt)*0.8, self.stability_analysis(eq),
                    ha='center', fontsize=10, color='red')
        plt.xlabel('Proportion using Open Foraging', fontsize=12)
        plt.ylabel('Rate of change (dx/dt)', fontsize=12)
        plt.title('Kiwi Foraging Strategy Dynamics - Phase Portrait', fontsize=14)
        plt.legend(fontsize=11)
        plt.grid(True, alpha=0.3)
        plt.tight_layout()
        plt.show()

    def plot_payoff_functions(self):
        x_range = np.linspace(0, 1, 100)
        plt.figure(figsize=(10, 6))
        plt.plot(x_range, [self.payoff_A(x) for x in x_range], 'b-',
                label='Open Foraging (A)', linewidth=2)
        plt.plot(x_range, [self.payoff_B(x) for x in x_range], 'r-',
                label='Cover Foraging (B)', linewidth=2)
        plt.plot(x_range, [self.average_payoff(x) for x in x_range], 'g--',
                label='Population Average', linewidth=2)
        eq = self.find_equilibrium()
        if eq is not None and 0 < eq < 1:
            plt.axvline(x=eq, color='purple', linestyle=':', alpha=0.7, linewidth=2)
            plt.plot(eq, self.average_payoff(eq), 'ro', markersize=10,
                    label=f'Equilibrium (x={eq:.3f})')
        plt.xlabel('Proportion using Open Foraging', fontsize=12)
        plt.ylabel('Expected Payoff (offspring per year)', fontsize=12)
        plt.title('Kiwi Foraging Strategy Payoffs', fontsize=14)
        plt.legend(fontsize=11)
        plt.grid(True, alpha=0.3)
        plt.tight_layout()
        plt.show()


